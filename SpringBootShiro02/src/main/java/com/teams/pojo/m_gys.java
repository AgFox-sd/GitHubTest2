package com.teams.pojo;

public class m_gys {
private String bh;
private String mc;
private String lx;
private String dc;
private String sl;
private String dw;
public m_gys() {
	super();
}
public m_gys(String bh, String mc, String lx, String dc, String sl, String dw) {
	super();
	this.bh = bh;
	this.mc = mc;
	this.lx = lx;
	this.dc = dc;
	this.sl = sl;
	this.dw = dw;
}
public String getBh() {
	return bh;
}
public void setBh(String bh) {
	this.bh = bh;
}
public String getMc() {
	return mc;
}
public void setMc(String mc) {
	this.mc = mc;
}
public String getLx() {
	return lx;
}
public void setLx(String lx) {
	this.lx = lx;
}
public String getDc() {
	return dc;
}
public void setDc(String dc) {
	this.dc = dc;
}
public String getSl() {
	return sl;
}
public void setSl(String sl) {
	this.sl = sl;
}
public String getDw() {
	return dw;
}
public void setDw(String dw) {
	this.dw = dw;
}

}
