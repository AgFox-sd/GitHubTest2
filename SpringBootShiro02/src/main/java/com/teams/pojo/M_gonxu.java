package com.teams.pojo;

public class M_gonxu {
//工序步骤表
	private int id;
	private String name;
	public M_gonxu() {
		super();
	}
	public M_gonxu(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
